from src.database.config import supabase
from datetime import datetime, timedelta

import bcrypt


# Convert plain password into encrypted password before storing in database
def hash_pass(pwd):
    return bcrypt.hashpw(pwd.encode(), bcrypt.gensalt()).decode()


# Verify entered password with stored encrypted password
def check_pass(pwd, hashed):
    return bcrypt.checkpw(pwd.encode(), hashed.encode())


# Check whether username already exists
# Used during teacher registration
def check_teacher_exists(username):
    response = supabase.table("teachers").select("username").eq("username", username).execute()
    return len(response.data) > 0


# Create new teacher account
# Password is stored in encrypted format
def create_teacher(username, password, name):

    data = {
        "username": username,
        "password": hash_pass(password),
        "name": name
    }

    response = supabase.table("teachers").insert(data).execute()
    return response.data


# Teacher login using username and password
def teacher_login(username, password):

    response = supabase.table("teachers").select("*").eq("username", username).execute()

    if response.data:
        teacher = response.data[0]

        if check_pass(password, teacher['password']):
            return teacher

    return None


