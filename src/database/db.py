# Fetch all subjects created by a teacher
# Also calculate total students and total attendance sessions
def get_teacher_subjects(teacher_id):

    response = supabase.table('subjects').select(
        "*, subject_students(count), attendance_logs(timestamp)"
    ).eq("teacher_id", teacher_id).execute()

    subjects = response.data

    for sub in subjects:

        # Count total enrolled students
        sub['total_students'] = (
            sub.get("subject_students", [{}])[0].get('count', 0)
            if sub.get('subject_students')
            else 0
        )

        # Count unique attendance sessions
        attendance = sub.get('attendance_logs', [])

        unique_sessions = len(
            set(log['timestamp'] for log in attendance)
        )

        sub['total_classes'] = unique_sessions

        # Remove unwanted nested data
        sub.pop('subject_students', None)
        sub.pop('attendance_logs', None)

    return subjects


# Enroll student into a subject
# subject_students acts as junction table
def enroll_student_to_subject(student_id, subject_id):

    data = {
        'student_id': student_id,
        "subject_id": subject_id
    }

    response = supabase.table('subject_students').insert(data).execute()
    return response.data


# Remove student from a subject
def unenroll_student_to_subject(student_id, subject_id):

    response = supabase.table('subject_students').delete().eq(
        'student_id', student_id
    ).eq(
        'subject_id', subject_id
    ).execute()

    return response.data


